package com.fil.classes;

public class OuterClass {
	
	private String name;
	private InnerClass inner;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public InnerClass getInner() {
		return inner;
	}
	public void setInner(InnerClass inner) {
		this.inner = inner;
	}
	

}
